package com.studenthackvii.dave;

public class Rating {
    private int id;
    private int review;


    public void setId(int id) {
        this.id = id;
    }

    public void setReview(int review) {
        this.review = review;
    }

    public int getId() {
        return id;
    }

    public int getReview() {
        return review;
    }
}
